package com.victorem.zamzamchains.retail.api.support;

public class ChainSupport {

	private String clientName;
	private String chainName;
	private double touch;
	private double grossWeight;
	private double yourTouch;
	private String date;
	private String number;

	public String getClientName() {
		return clientName;
	}

	public void setClientName(String clientName) {
		this.clientName = clientName;
	}

	public String getChainName() {
		return chainName;
	}

	public void setChainName(String chainName) {
		this.chainName = chainName;
	}

	public double getTouch() {
		return touch;
	}

	public void setTouch(double touch) {
		this.touch = touch;
	}

	public double getGrossWeight() {
		return grossWeight;
	}

	public void setGrossWeight(double grossWeight) {
		this.grossWeight = grossWeight;
	}

	public double getYourTouch() {
		return yourTouch;
	}

	public void setYourTouch(double yourTouch) {
		this.yourTouch = yourTouch;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

}
